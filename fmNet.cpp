#include "fmDM.h"

//----------------------------------------------------------------------------------------------------------------------//
fmNet_C::fmNet_C(string p_strName) : m_strName(p_strName), m_nNetWeight(1)
{
    m_vPart.resize(2);
    m_vPart[0] = 0;
    m_vPart[1] = 0;
}
//----------------------------------------------------------------------------------------------------------------------//
string fmNet_C::getName()
{
    return m_strName;
}
//----------------------------------------------------------------------------------------------------------------------//
int fmNet_C::getNetWeight()
{
    return m_nNetWeight;
}
//----------------------------------------------------------------------------------------------------------------------//
void fmNet_C::addNode(fmNode_C* p_pNode)
{
    m_vNode.push_back(p_pNode);
}
//----------------------------------------------------------------------------------------------------------------------//
int fmNet_C::getNodeNum()
{
    return m_vNode.size();
}
//----------------------------------------------------------------------------------------------------------------------//
fmNode_C* fmNet_C::getNode(int p_nNodeIdx)
{
    return m_vNode[p_nNodeIdx];
}
//----------------------------------------------------------------------------------------------------------------------//
int fmNet_C::getPart(int p_nPart)
{
    return m_vPart[p_nPart];
}
//----------------------------------------------------------------------------------------------------------------------//
void fmNet_C::setPart(int p_nPart, int p_nNumNode)
{
    m_vPart[p_nPart] = p_nNumNode;
}
//----------------------------------------------------------------------------------------------------------------------//
void fmNet_C::addPart(int p_nPart, int p_nNumNode)
{
    m_vPart[p_nPart] += p_nNumNode;
}
